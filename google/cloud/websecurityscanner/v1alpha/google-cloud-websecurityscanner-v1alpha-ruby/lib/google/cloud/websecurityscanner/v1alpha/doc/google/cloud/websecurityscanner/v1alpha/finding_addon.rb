module Google
  module Cloud
    module Websecurityscanner
      module V1alpha
        # Information reported for an outdated library.
        # @!attribute [rw] library_name
        #   @return [String]
        #     The name of the outdated library.
        # @!attribute [rw] version
        #   @return [String]
        #     The version number.
        # @!attribute [rw] learn_more_urls
        #   @return [Array<String>]
        #     URLs to learn more information about the vulnerabilities in the library.
        class OutdatedLibrary; end

        # Information regarding any resource causing the vulnerability such
        # as JavaScript sources, image, audio files, etc.
        # @!attribute [rw] content_type
        #   @return [String]
        #     The MIME type of this resource.
        # @!attribute [rw] resource_url
        #   @return [String]
        #     URL of this violating resource.
        class ViolatingResource; end

        # Information about vulnerable request parameters.
        # @!attribute [rw] parameter_names
        #   @return [Array<String>]
        #     The vulnerable parameter names.
        class VulnerableParameters; end

        # Information about vulnerable or missing HTTP Headers.
        # @!attribute [rw] headers
        #   @return [Array<Google::Cloud::Websecurityscanner::V1alpha::VulnerableHeaders::Header>]
        #     List of vulnerable headers.
        # @!attribute [rw] missing_headers
        #   @return [Array<Google::Cloud::Websecurityscanner::V1alpha::VulnerableHeaders::Header>]
        #     List of missing headers.
        class VulnerableHeaders
          # Describes a HTTP Header.
          # @!attribute [rw] name
          #   @return [String]
          #     Header name.
          # @!attribute [rw] value
          #   @return [String]
          #     Header value.
          class Header; end
        end

        # Information reported for an XSS.
        # @!attribute [rw] stack_traces
        #   @return [Array<String>]
        #     Stack traces leading to the point where the XSS occurred.
        # @!attribute [rw] error_message
        #   @return [String]
        #     An error message generated by a javascript breakage.
        class Xss; end
      end
    end
  end
end